#include "pch.h"

#include "../crypto/hex.h"
using crypto::encode::Hex;

TEST(Hex, encode)
{
	EXPECT_EQ(Hex::encode("!Q}F$WCk"), "21517D462457436B");
	EXPECT_EQ(Hex::encode("~D!@e78<2K%O=Ayb1l>S;9MUp`Lv"), "7E4421406537383C324B254F3D417962316C3E533B394D5570604C76");
	EXPECT_EQ(Hex::encode("uh/MD7VuiDcl#SKIk>+QLiZ='p|_RuOjE,d<%29KiQ4}h8A#hprv`5Y!yU7Sy;;07NSGgu{!~5_!hA4[t8]UdN|e+jOSA9H(TH#S,94D-D_D)d<9lgae#EC6-wd9n,"), "75682F4D443756756944636C23534B496B3E2B514C695A3D27707C5F52754F6A452C643C2532394B6951347D68384123687072766035592179553753793B3B30374E534767757B217E355F216841345B74385D55644E7C652B6A4F5341394828544823532C3934442D445F4429643C396C676165234543362D7764396E2C");
}

TEST(Hex, decode)
{
	EXPECT_EQ(bytes2str(Hex::decode("21517D462457436B")), "!Q}F$WCk");
	EXPECT_EQ(bytes2str(Hex::decode("7E4421406537383C324B254F3D417962316C3E533B394D5570604C76")), "~D!@e78<2K%O=Ayb1l>S;9MUp`Lv");
	EXPECT_EQ(bytes2str(Hex::decode("75682F4D443756756944636C23534B496B3E2B514C695A3D27707C5F52754F6A452C643C2532394B6951347D68384123687072766035592179553753793B3B30374E534767757B217E355F216841345B74385D55644E7C652B6A4F5341394828544823532C3934442D445F4429643C396C676165234543362D7764396E2C")), "uh/MD7VuiDcl#SKIk>+QLiZ='p|_RuOjE,d<%29KiQ4}h8A#hprv`5Y!yU7Sy;;07NSGgu{!~5_!hA4[t8]UdN|e+jOSA9H(TH#S,94D-D_D)d<9lgae#EC6-wd9n,");
}
